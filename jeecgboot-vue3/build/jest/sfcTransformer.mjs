import { parse, compileScript, compileTemplate } from '@vue/compiler-sfc';
import { transformSync } from '@babel/core';

export function process(src, filename) {
  const { descriptor, errors } = parse(src, { filename });
  if (errors.length) {
    throw errors[0];
  }

  const id = 'data-v-' + Math.random().toString(36).slice(2, 8);
  const hasScoped = descriptor.styles.some((s) => s.scoped);

  const scopeId = hasScoped ? id : undefined;

  const scriptResult = compileScript(descriptor, {
    id,
    inlineTemplate: false,
    genDefaultAs: '_sfc_main_',
    templateOptions: {
      scoped: hasScoped,
      compilerOptions: {
        scopeId,
      },
    },
  });

  let code = scriptResult.content;

  // Fix: remove __expose() call, expose parameter, and __isScriptSetup flag
  // generated by compileScript for <script setup>. In Vue 3.5.27, the
  // __isScriptSetup flag causes PublicInstanceProxyHandlers.get() to skip
  // setup bindings (hasSetupBinding returns false), making _ctx.xxx undefined
  // in the compiled render function. Removing the flag exposes bindings on ctx
  // so the render function can access them through the proxy.
  code = code.replace(
    /\{ *expose: *__expose *\}/g,
    '{} /* expose param removed */',
  );
  code = code.replace(
    /\b__expose\(\s*\)\s*;/g,
    '/* __expose() removed */;',
  );
  code = code.replace(
    /Object\.defineProperty\(__returned__,\s*'__isScriptSetup',\s*\{[^}]*\}\s*\);?/g,
    '/* __isScriptSetup removed */',
  );

  if (descriptor.template) {
    const templateResult = compileTemplate({
      source: descriptor.template.content,
      filename,
      id,
      scoped: hasScoped,
      slotted: descriptor.slotted,
      compilerOptions: {
        scopeId,
      },
    });
    if (templateResult.errors.length) {
      throw templateResult.errors[0];
    }
    code += '\n' + templateResult.code + '\n';
    code += '_sfc_main_.render = render;\n';
  }

  if (hasScoped) {
    code += '_sfc_main_.__scopeId = "' + id + '";\n';
  }

  code += '\nmodule.exports = _sfc_main_;\n';

  const cjs = transformSync(code, {
    babelrc: false,
    configFile: false,
    filename,
    plugins: ['@babel/plugin-transform-typescript', '@babel/plugin-transform-modules-commonjs'],
    sourceType: 'module',
  });

  return cjs.code;
}

export default {
  process(src, filename) {
    try {
      const code = process(src, filename);
      return { code };
    } catch (e) {
      const message = e && e.message ? e.message : String(e);
      throw new Error('SFC transform failed for ' + filename + ': ' + message);
    }
  },
};
